function getArrayStats() {
    let numbers = [];
    for (let i = 0; i < 5; i++) {
        let num = parseFloat(prompt(`–í–≤–µ–¥–∏—Ç–µ —á–∏—Å–ª–æ ${i + 1}:`));
        if (!isNaN(num)) {
            numbers.push(num);
        } else {
            console.log("–û—à–∏–±–∫–∞! –í–≤–µ–¥–µ–Ω–æ –Ω–µ —á–∏—Å–ª–æ.");
            alert("–û—à–∏–±–∫–∞! –í–≤–µ–¥–∏—Ç–µ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–µ —á–∏—Å–ª–æ.");
            return;
        }
    }

    let sum = numbers.reduce((acc, num) => acc + num, 0);
    let avg = sum / numbers.length;
    
    console.log("–í–≤–µ–¥–µ–Ω–Ω—ã–π –º–∞—Å—Å–∏–≤:", numbers);
    console.log("–°—É–º–º–∞ —ç–ª–µ–º–µ–Ω—Ç–æ–≤:", sum);
    console.log("–°—Ä–µ–¥–Ω–µ–µ –∑–Ω–∞—á–µ–Ω–∏–µ:", avg.toFixed(2));

    document.getElementById("arrayStatsResult").innerHTML = `üìå –°—É–º–º–∞: ${sum}, –°—Ä–µ–¥–Ω–µ–µ: ${avg.toFixed(2)}`;
}

